numeroA = float(input("Digite o primeiro número: "))
numeroB = float(input("Digite o segundo número: "))
if (numeroA%numeroB==0):
    print("O numéro A (",numeroA,") é divisível pelo número B (",numeroB,")")
else:
    print("O numéro A (", numeroA, ") não é divisível pelo número B (", numeroB, ")")